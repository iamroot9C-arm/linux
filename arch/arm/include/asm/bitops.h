/** 20130427    
 * MSB 부터 0이 아닌 첫번째 위치를 구함.
 *	(index는 32 ... 1)
 **/
static inline int constant_fls(int x)
{
	int r = 32;

	if (!x)
		return 0;
	if (!(x & 0xffff0000u)) {
		x <<= 16;
		r -= 16;
	}
	if (!(x & 0xff000000u)) {
		x <<= 8;
		r -= 8;
	}
	if (!(x & 0xf0000000u)) {
		x <<= 4;
		r -= 4;
	}
	if (!(x & 0xc0000000u)) {
		x <<= 2;
		r -= 2;
	}
	if (!(x & 0x80000000u)) {
		x <<= 1;
		r -= 1;
	}
	return r;
}

/*
 * On ARMv5 and above those functions can be implemented around
 * the clz instruction for much better code efficiency.
 */
/** 20130323
*	MSB 부터 0이 아닌 첫번째 위치를 구함. (find last set)
*	(index는 32 ... 1)
*/
static inline int fls(int x)
{
	int ret;

	/** 20130323
	* 상수면 compiler time 에 fls가 계산됨.
	**/
	if (__builtin_constant_p(x))
	       return constant_fls(x);

	/** 20130323
	*	clz : count leading zero (from MSB)
	*   ret : 32 - clz 
	*/
	asm("clz\t%0, %1" : "=r" (ret) : "r" (x));
       	ret = 32 - ret;
	return ret;
}

/** 20130427    
 * __t & -__t : 2의 보수와 비트 and.
 *   -> LSB부터 0이 아닌 첫번째 비트만 남겨두고 나머지 비트는 0.
 *   ex) 0x00000080
 *     & 0xffffff80
 *     ------------
 *       0x00000080
 *
 *   ex) 0x00000180
 *     & 0xfffffe80
 *     ------------
 *       0x00000080
 *
 *  fls는 MSB부터 첫번째 1이 나오는 zero base offset + 1.
 *  __ffs는 fls를 다시 zero base로 만듦.
 *
 *  ex) ffz(0xffffff7f)           __ffs(0x80)
 *      __ffs(0x80)               (ffs(0x80) - 1)
 *      (ffs(0x80) - 1)           (fls(0x00000080 & 0xffffff80) - 1)
 *                                (fls(0x80) - 1)
 *                                (8 - 1) = 7
 *
 * find first zero (ffz)
 *   from: http://en.wikipedia.org/wiki/Find_first_set
 *
 **/
#define __fls(x) (fls(x) - 1)
#define ffs(x) ({ unsigned long __t = (x); fls(__t & -__t); })
#define __ffs(x) (ffs(x) - 1)
#define ffz(x) __ffs( ~(x) )

#endif

